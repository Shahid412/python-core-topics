tuple = [ 'xyz', 111 , 201, 9.2, 'blank', 70.2, 'king' ]

small_tuple = [100, 'chip', 0.01]

# Print complete tuple
print (tuple)

# Print first element of the tuple
print (tuple[0])

# Print elements starting from 2nd till 3rd
print (tuple[1:3])

# Print elements starting from 3rd element
print (tuple[2:])

# Print tuple two times
print (small_tuple * 2 )

# Print concatenated tuples
print (tuple + small_tuple) 