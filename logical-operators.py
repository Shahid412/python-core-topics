a = 100
b = 20
list = [1, 2, 3, 4, 5 ]


# Logical operators
if ( a in list and b in list):
	print("1 - and - both a and b are available in the given list")
else:
	print("1 - and - either a or b is not available in the given list")

# Logical not 
if ( a not in list ):
	print("2 - not - a is NOT available in the given list")
else:
	print("2 - not - a is available in the given list")




