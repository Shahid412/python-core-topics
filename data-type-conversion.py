# Convert x to an integer. bastringe specifies the base if x is a string.
print(int(z))

# Convert x to a floating-point number.
print(float(y))

# Create a complex number.
print(complex(z))

# Convert object x to a string representation.
print(str(y))

# Convert object x to an expression string.
print(repr(z))

# Convert string to a tuple
print(tuple(x))

# Convert string to a list.
print(list(string))

# Convert string to a set.
print(set(string))

# Convert string to a frozen set.
print(frozenset(string))

# Convert an integer to a character.
print(chr(y))

# Convert a single character to its integer value.
print(ord(a))

# Convert an integer to a hexadecimal string.
print(hex(y))

# Convert an integer/float to an octal string.
